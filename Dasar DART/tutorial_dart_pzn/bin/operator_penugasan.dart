void main() {
  ///semua operator aritmatika bisa di lakukan dengan cara ini
  /**
   * +
   * -
   * /
   * ~/
   * *
   * %
   */

  var a = 10;

  print(a += 1); // = a = a + 1;

  // bisa juga menggunakakn
  var k = 1;
  var j = ++k; // k + 1, j = k

  print(k);
  print(j);

  var x = 3;
  var y = x++; // y = x, x + 1
  print(x);
  print(y);
}

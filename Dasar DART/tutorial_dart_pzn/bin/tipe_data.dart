void main() {
  //NOMOR
  //number: int = bilangan bulat dan double = bilangan pecahan
  int no1 = 10;
  double no2 = 10.3;
  print(no1);
  print(no2);

  //num: tipe datanya bisa int dan bisa double
  num no = 20;
  print(no);

  no = 20.3;
  print(no);

  //boolean: cuman ada 2 buah nilai yaitu true dan flase
  bool status = true;
  print(status);

  status = false;
  print(status);

  //STRING
  //string interpolation
  var namaPertama = 'Akhiro';
  var namaAkhir = 'Siro';

  var namaLengkap = '$namaPertama ${namaAkhir}';

  print(namaLengkap);

  //penggabungan string
  var nama1 = namaPertama + namaAkhir;
  var nama2 = 'Akhiro' 'Siro';

  print(nama1);
  print(nama2);
}
